package edu.uc.cs.distsys.init;

public class GroupJoinException extends Exception {

	private static final long serialVersionUID = 9101885102248229153L;

	public GroupJoinException() {
	}

	public GroupJoinException(String arg0) {
		super(arg0);
	}

	public GroupJoinException(Throwable arg0) {
		super(arg0);
	}

	public GroupJoinException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GroupJoinException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super();
	}

}
